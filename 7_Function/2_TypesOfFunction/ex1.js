/*
1. Named Functions
-------------------
These functions have a name and are defined using the function keyword.

function add(a, b) {
  return a + b;
}
==============================================================================
2. Anonymous Functions
-----------------------
These functions do not have a name and are often used as arguments to other functions.

const sum = function(a, b) {
  return a + b;
};
==============================================================================
3. Arrow Functions
--------------------
Introduced in ES6, arrow functions provide a more concise syntax for writing functions and inherit 
the this context from their parent scope.

const add = (a, b) => a + b;
===============================================================================
*/
